class RPG::UsableItem < RPG::BaseItem
  include EBJB
  
  #//////////////////////////////////////////////////////////////////////////
  # * Properties
  #//////////////////////////////////////////////////////////////////////////
  
  #--------------------------------------------------------------------------
  # * Determines whether or not selection of the target is required
  #--------------------------------------------------------------------------
  # GET
  def need_selection?
    # Default is 1, 3, 7, 9
    return [1, 3, 7, 9, 2, 8, 10].include?(@scope)
  end
  
end
